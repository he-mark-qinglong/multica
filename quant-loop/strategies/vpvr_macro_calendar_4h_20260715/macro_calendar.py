def high_impact_event_dates() -> List[pd.Timestamp]:
    """Return the sorted list of high-impact event dates."""
    return list(_DAYS)


def is_high_impact_event_date(ts: pd.Timestamp) -> bool:
    """Return True if `ts` (any time on that UTC calendar day) is an event date."""
    if ts is None:
        return False
    return ts.normalize() in _DAYS